    boolean CheckColonne(Jeton j){
        boolean check = false;
        int cpt = 0;
  
        for(int i=0; i <= SizeX-1; i++){
            if(Plateau[i][j.getColonne()].getJoueur() == j.getJoueur()){
                cpt++;
            }
            else{
                cpt =0;
            }
            
            if(cpt == 4){
                check = true;
            }
        }
        

        return check;
    }
    
